package pkg100days;

public class Day25 {
    public static void main(String[] args) {
        System.out.println("====INCREMENT====");
        for (int i = 1; i <= 10; i++) {
            System.out.println("tampilkan increment: " + i);
        }
        System.out.println("====DECREMENT====");
        for (int j = 10; j > 1; j--) {
            System.out.println("tampilkan decrement: " + j);
            
            
        
    }
    }
}
