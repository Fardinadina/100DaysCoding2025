package pkg100days;

public class Day26 {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(" nilai a normal : "+ a);
        
        a += 5;
        System.out.println(" setelah a += 5  : "+ a);
        
        a -= 5;
        System.out.println(" setelah a -= 5   : "+ a);
        
        a *= 5;
        System.out.println(" setelah a *= 5   : "+ a);
        
        a /= 5;
        System.out.println(" setelah a /= 5   : "+ a);
        
        a %= 5;
        System.out.println(" setelah a %= 5   : "+ a);
        
    }
}
